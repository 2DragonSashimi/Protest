# [28. 실전 템플릿 1 - 좌표 이동 시뮬레이션]
# --------------------------------------------------
def simulate_moves(board, start_r, start_c, start_d, commands):
    rows = len(board)
    cols = len(board[0])

    dr = [-1, 0, 1, 0]
    dc = [0, 1, 0, -1]

    r, c, d = start_r, start_c, start_d

    for cmd in commands:
        if cmd == "L":
            d = (d - 1) % 4
        elif cmd == "R":
            d = (d + 1) % 4
        elif cmd == "F":
            nr = r + dr[d]
            nc = c + dc[d]
            if 0 <= nr < rows and 0 <= nc < cols and board[nr][nc] != "#":
                r, c = nr, nc

    return r, c, d


