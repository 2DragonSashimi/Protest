# [14. 시간 흐름 시뮬레이션]
# --------------------------------------------------
# Time Based API와 비슷하지만,
# 구현 문제에서는 시간 단위로 이벤트를 처리하는 경우가 많다.

def simulate_time_events(events):
    # events: (time, action)
    now = 0
    log = []

    for t, action in events:
        while now < t:
            now += 1
            # 매 시간마다 자동 처리할 일이 있다면 여기서 처리
        log.append((now, action))

    return log

