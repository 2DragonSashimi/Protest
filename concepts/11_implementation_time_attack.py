# [21. 동시 갱신 vs 순차 갱신]
# --------------------------------------------------
# 구현 문제에서 매우 중요하다.
#
# 순차 갱신:
# - 앞에서 바뀐 결과가 뒤에 바로 영향을 줌
#
# 동시 갱신:
# - 이번 턴의 모든 변화는 "기존 상태"를 기준으로 계산
# - 계산 후 한꺼번에 반영
#
# 동시 갱신이면 보통 next_board 같은 별도 배열이 필요하다.

def next_generation(board):
    rows = len(board)
    cols = len(board[0])
    new_board = [row[:] for row in board]

    for r in range(rows):
        for c in range(cols):
            # 예시 규칙
            new_board[r][c] = board[r][c]

    return new_board


