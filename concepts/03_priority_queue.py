# --------------------------------------------------
# [15. 시간복잡도]
# --------------------------------------------------
# heapq 기준 대략:
#
# - heappush   : O(log N)
# - heappop    : O(log N)
# - hq[0] 확인 : O(1)
# - heapify    : O(N)
#
# 따라서,
# - 계속 삽입/삭제하면서 최솟값 관리 -> 힙 적합
# - 한 번만 정렬해서 끝 -> sort도 충분히 가능


# --------------------------------------------------
# [16. sort와 Priority Queue의 차이]
# --------------------------------------------------
# 둘 다 정렬/순서와 관련 있지만 성격이 다르다.
#
# sort:
# - 한 번에 전체 정렬
# - 이후 꺼내는 것은 쉬움
# - 중간 삽입이 잦으면 비효율적
#
# Priority Queue:
# - 계속 값이 들어오고 나가도 관리 가능
# - 전체 정렬 상태를 보장하지는 않음
# - 최솟값/최댓값만 빠르게 관리
#
# 즉,
# "전체를 한 번 정렬"이면 sort,
# "동적으로 최솟값/최댓값 반복 관리"면 PQ를 떠올린다.


# --------------------------------------------------
# [17. 대표 문제 유형]
# --------------------------------------------------
# Priority Queue는 아래 유형으로 자주 나온다.
#
# 1) 가장 작은 값 / 큰 값 반복 추출
#    - 최소값 계속 꺼내기
#    - 최대값 계속 꺼내기
#
# 2) top-k 유지
#    - 가장 큰 k개
#    - 가장 작은 k개
#
# 3) 최소 비용 결합
#    - 카드 합치기
#    - 파일 합치기 느낌
#
# 4) 작업 / 이벤트 스케줄링
#    - 우선순위 높은 작업 먼저
#    - 빠른 이벤트 먼저
#
# 5) 그래프
#    - 다익스트라
#
# 6) 시뮬레이션
#    - 특정 우선순위 기준으로 상태 처리


# --------------------------------------------------
# [18. 자주 하는 실수]
# --------------------------------------------------
# 1) Python heapq는 "최소 힙"이다.
#    최대 힙이 기본이 아니다.
#
# 2) 최대 힙 흉내낼 때 음수 처리 후
#    꺼낼 때 다시 부호를 되돌리는 걸 잊기 쉽다.
#
# 3) 힙 내부 리스트는 "전체 정렬된 상태"가 아니다.
#    hq를 그대로 출력했다고 정렬된 배열처럼 믿으면 안 된다.
#
# 4) 튜플 힙에서 비교 불가능한 값이 들어가면 주의해야 한다.
#    예를 들어 앞 원소가 같을 때 뒤 원소끼리 비교가 일어나는데,
#    뒤 원소 타입이 비교 불가능하면 문제가 생길 수 있다.
#
# 5) 빈 힙에서 heappop 하면 에러가 난다.
#
# 6) 정렬 한 번이면 충분한 문제에 굳이 힙을 써서
#    코드만 복잡해질 수 있다.
#
# 7) top-k 유지에서 최소 힙/최대 힙 방향을 반대로 잡는 실수를 자주 한다.


