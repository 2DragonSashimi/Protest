# 03. Priority Queue
# ============================================

# --------------------------------------------------
# [1. 왜 필요한가]
# --------------------------------------------------
# Priority Queue(우선순위 큐)는
# "가장 우선순위가 높은 원소를 빠르게 꺼내기 위해" 사용한다.
#
# 일반 큐는 먼저 들어온 것이 먼저 나가지만(FIFO),
# 우선순위 큐는 값의 크기나 특정 기준에 따라 먼저 나갈 원소가 결정된다.
#
# 예:
# - 가장 작은 값 / 가장 큰 값 반복적으로 꺼내기
# - 작업 스케줄링
# - top-k 유지
# - 다익스트라
# - 시뮬레이션 문제에서 현재 가장 중요한 상태 처리
#
# 코테에서는 정렬만으로는 부족하고,
# "원소가 계속 추가/삭제되면서 최솟값/최댓값을 빠르게 관리"해야 할 때 매우 자주 쓴다.


# --------------------------------------------------
# [2. 언제 떠올려야 하는가]
# --------------------------------------------------
# 문제를 읽다가 아래 신호가 보이면 Priority Queue를 먼저 의심한다.
#
# - 가장 작은 값 / 큰 값을 반복해서 꺼내야 한다
# - 우선순위가 가장 높은 작업부터 처리해야 한다
# - 원소가 계속 들어오고 빠지는 상황이다
# - 매 순간 top 1 / top k 를 알아야 한다
# - 정렬된 상태를 계속 유지해야 한다
# - "현재 최솟값/최댓값을 빨리 꺼내라"가 핵심이다
#
# 대표 예시:
# - 카드 합치기
# - 회의실 / 강의실 배정
# - 작업 스케줄링
# - 다익스트라 최단경로
# - median 유지 문제의 일부 변형
# - 시뮬레이션에서 이벤트 우선 처리


# --------------------------------------------------
# [3. 핵심 아이디어]
# --------------------------------------------------
# 우선순위 큐의 핵심은 아래 정도로 보면 된다.
#
# 1. 최소 힙(min heap)
#    - 가장 작은 값이 먼저 나온다
#
# 2. 최대 힙(max heap)
#    - 가장 큰 값이 먼저 나오게 만들 수 있다
#
# 3. 튜플 우선순위
#    - (우선순위, 값) 형태로 저장 가능
#
# 4. 동적 top-k 관리
#    - 계속 들어오는 데이터 중 상위 일부만 유지 가능
#
# 5. 그래프/시뮬레이션 연결
#    - 다익스트라, 이벤트 처리 등에 자주 쓰인다


import heapq


# --------------------------------------------------
# [4. Python에서의 우선순위 큐]
# --------------------------------------------------
# Python에서는 heapq를 사용한다.
# 중요한 점:
# "기본적으로 최소 힙(min heap)" 이다.

hq = []
heapq.heappush(hq, 3)
heapq.heappush(hq, 1)
heapq.heappush(hq, 5)

a = heapq.heappop(hq)   # 1
b = heapq.heappop(hq)   # 3
c = heapq.heappop(hq)   # 5


# --------------------------------------------------
# [5. 최소 힙]
# --------------------------------------------------
# 가장 작은 값이 먼저 나오는 구조이다.

min_heap = []
for x in [4, 2, 7, 1, 9]:
    heapq.heappush(min_heap, x)

top_min = min_heap[0]            # 현재 최소값 확인
pop_min = heapq.heappop(min_heap)  # 최소값 꺼내기

# 자주 쓰는 연산
# - heapq.heappush(hq, x)
# - heapq.heappop(hq)
# - hq[0]   : 현재 최소값 보기 (비어있지 않을 때)


# --------------------------------------------------
# [6. 최대 힙]
# --------------------------------------------------
# heapq는 기본이 최소 힙이므로,
# 최대 힙처럼 쓰려면 보통 음수를 넣는다.

max_heap = []
for x in [4, 2, 7, 1, 9]:
    heapq.heappush(max_heap, -x)

top_max = -max_heap[0]             # 현재 최대값
pop_max = -heapq.heappop(max_heap) # 최대값 꺼내기

# 정리:
# - 넣을 때 음수
# - 꺼낼 때 다시 음수 부호 제거


# --------------------------------------------------
# [7. 튜플 힙]
# --------------------------------------------------
# 우선순위 외에 추가 정보를 같이 저장할 수 있다.
# Python의 튜플 비교는 앞 원소부터 순서대로 이루어진다.

pq = []
heapq.heappush(pq, (2, "task_b"))
heapq.heappush(pq, (1, "task_a"))
heapq.heappush(pq, (3, "task_c"))

first_task = heapq.heappop(pq)   # (1, "task_a")

# 즉, (우선순위, 값) 형태로 많이 쓴다.
#
# 예:
# - (거리, 노드)
# - (시간, 이벤트)
# - (점수, 이름)
# - (비용, 상태)


# --------------------------------------------------
# [8. heapify]
# --------------------------------------------------
# 이미 리스트가 있으면 heapify로 힙으로 바꿀 수 있다.

arr = [5, 2, 8, 1, 3]
heapq.heapify(arr)

# 이제 arr는 힙 구조가 된다.
smallest = heapq.heappop(arr)   # 1

# O(N)으로 힙을 만들 수 있다.
# 원소를 하나씩 push하면 O(N log N)이므로,
# 이미 리스트가 있으면 heapify가 더 효율적일 수 있다.


# --------------------------------------------------
# [9. heappushpop / heapreplace]
# --------------------------------------------------
# 둘 다 push와 pop을 합친 연산이다.

hq = [2, 4, 6]
heapq.heapify(hq)

# heappushpop:
# 먼저 push 후, 가장 작은 값을 pop
x = heapq.heappushpop(hq, 3)

# heapreplace:
# 가장 작은 값을 pop한 뒤, 새 값을 push
y = heapq.heapreplace(hq, 5)

# 둘은 비슷해 보이지만 동작 순서가 다르므로 상황에 따라 주의해야 한다.


# --------------------------------------------------
# [10. 가장 자주 쓰는 패턴 1 - 최솟값 반복 추출]
# --------------------------------------------------
# 원소들을 오름차순으로 하나씩 꺼내고 싶다면 힙이 유용하다.

nums = [7, 2, 5, 1, 9]
heapq.heapify(nums)

sorted_by_heap = []
while nums:
    sorted_by_heap.append(heapq.heappop(nums))

# 결과: [1, 2, 5, 7, 9]

# 물론 그냥 sort()도 가능하지만,
# "중간중간 새 원소가 들어오는 상황"에서는 힙이 더 자연스럽다.


# --------------------------------------------------
# [11. 가장 자주 쓰는 패턴 2 - top k 유지]
# --------------------------------------------------
# 많은 값 중 가장 큰 k개만 유지하고 싶을 때
# 크기 k의 최소 힙을 유지하면 된다.

def keep_top_k(nums, k):
    hq = []
    for x in nums:
        if len(hq) < k:
            heapq.heappush(hq, x)
        else:
            if x > hq[0]:
                heapq.heapreplace(hq, x)
    return hq

top_k_heap = keep_top_k([5, 1, 9, 3, 7, 8], 3)

# top_k_heap 안에는 가장 큰 3개가 들어 있다.
# 단, 내부가 정렬된 리스트는 아니다.
# 필요하면 sorted(top_k_heap) 로 확인한다.


# --------------------------------------------------
# [12. 가장 자주 쓰는 패턴 3 - 최소 비용 합치기]
# --------------------------------------------------
# 우선순위 큐의 대표 문제 유형.
# 가장 작은 두 값을 계속 합치는 문제에서 자주 사용한다.

def merge_cost(nums):
    hq = nums[:]
    heapq.heapify(hq)

    total = 0
    while len(hq) > 1:
        a = heapq.heappop(hq)
        b = heapq.heappop(hq)
        cost = a + b
        total += cost
        heapq.heappush(hq, cost)

    return total

merge_result = merge_cost([10, 20, 40])

# 카드 정렬하기 / 파일 합치기 / 최적 병합 패턴 계열에서 자주 나온다.


# --------------------------------------------------
# [13. 가장 자주 쓰는 패턴 4 - 작업 스케줄링]
# --------------------------------------------------
# 우선순위가 가장 높은 작업부터 처리하는 문제.

tasks = []
heapq.heappush(tasks, (2, "low"))
heapq.heappush(tasks, (1, "high"))
heapq.heappush(tasks, (3, "later"))

order = []
while tasks:
    priority, name = heapq.heappop(tasks)
    order.append(name)

