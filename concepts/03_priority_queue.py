# [20. 실전 템플릿 1 - 최소 힙]
# --------------------------------------------------
def min_heap_example(nums):
    hq = []
    for x in nums:
        heapq.heappush(hq, x)

    result = []
    while hq:
        result.append(heapq.heappop(hq))
    return result

min_heap_result = min_heap_example([4, 2, 7, 1])


# --------------------------------------------------
# [21. 실전 템플릿 2 - 최대 힙]
# --------------------------------------------------
def max_heap_example(nums):
    hq = []
    for x in nums:
        heapq.heappush(hq, -x)

    result = []
    while hq:
        result.append(-heapq.heappop(hq))
    return result

max_heap_result = max_heap_example([4, 2, 7, 1])


# --------------------------------------------------
# [22. 실전 템플릿 3 - top k 큰 값 유지]
# --------------------------------------------------
def top_k_largest(nums, k):
    hq = []
    for x in nums:
        if len(hq) < k:
            heapq.heappush(hq, x)
        else:
            if x > hq[0]:
                heapq.heapreplace(hq, x)
    return sorted(hq)

top_k_result = top_k_largest([5, 1, 9, 3, 7, 8], 3)


# --------------------------------------------------
# [23. 실전 템플릿 4 - 우선순위 작업 처리]
# --------------------------------------------------
def process_tasks(task_list):
    pq = []
    for priority, name in task_list:
        heapq.heappush(pq, (priority, name))

    order = []
    while pq:
        priority, name = heapq.heappop(pq)
        order.append((priority, name))
    return order

task_result = process_tasks([(2, "low"), (1, "high"), (3, "later")])


# --------------------------------------------------
# [24. 실전 템플릿 5 - 최소 비용 합치기]
# --------------------------------------------------
def min_merge_cost(nums):
    hq = nums[:]
    heapq.heapify(hq)

    total = 0
    while len(hq) > 1:
        a = heapq.heappop(hq)
        b = heapq.heappop(hq)
        s = a + b
        total += s
        heapq.heappush(hq, s)

    return total

merge_cost_result = min_merge_cost([10, 20, 40])


