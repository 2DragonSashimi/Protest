# --------------------------------------------------
# [12. Counter]
# --------------------------------------------------
# 빈도수 세기에 특화.
# 많이 쓰는 기능:
# - 개수 확인
# - 가장 많이 나온 원소 찾기
# - 두 문자열/배열 빈도 비교

nums = [1, 1, 2, 3, 3, 3, 4]
cnt = Counter(nums)

count_1 = cnt[1]          # 2
count_3 = cnt[3]          # 3
top2 = cnt.most_common(2) # [(3, 3), (1, 2)]

# 문자열에서도 사용 가능
word_counter = Counter("mississippi")


# --------------------------------------------------
# [13. dict / set의 평균 시간복잡도]
# --------------------------------------------------
# 평균적으로 아래와 같다.
#
# - 삽입: O(1)
# - 조회: O(1)
# - 삭제: O(1)
#
# 그래서 빠른 조회가 필요하면 Hash를 먼저 생각한다.
#
# 단, 최악의 경우 O(N)이 될 수 있다는 이론적 설명은 있지만
# 코테에서는 일반적으로 평균 O(1)로 생각하면 된다.


