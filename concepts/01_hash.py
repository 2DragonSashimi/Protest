# 01. Hash
# ============================================

# --------------------------------------------------
# [1. 왜 필요한가]
# --------------------------------------------------
# Hash는 "빠른 조회"를 위해 사용한다.
#
# 예를 들어,
# - 어떤 값이 이미 나왔는지 확인
# - 각 값이 몇 번 나왔는지 세기
# - 특정 key에 대응하는 value 저장
#
# 이런 작업을 리스트로 하면 느리거나 불편할 수 있다.
# Python에서는 dict와 set이 Hash 기반 자료구조의 핵심이다.


# --------------------------------------------------
# [2. 언제 떠올려야 하는가]
# --------------------------------------------------
# 문제를 읽다가 아래 신호가 보이면 Hash를 먼저 의심한다.
#
# - 중복을 확인해야 한다
# - 등장 횟수를 세야 한다
# - 빠른 존재 여부 확인이 필요하다
# - 값 -> 인덱스 / 상태 / 개수 매핑이 필요하다
# - 문자열 / ID / 이름 같은 것을 key로 저장해야 한다
#
# 대표 예시:
# - 중복 원소 찾기
# - 빈도수 세기
# - two sum
# - 아나그램 판별
# - 출석 체크 / 유저 상태 관리


# --------------------------------------------------
# [3. 핵심 도구]
# --------------------------------------------------
# Python에서 Hash 관련 핵심 도구는 아래 4개다.
#
# 1. dict
#    key -> value 저장
#
# 2. set
#    중복 없는 값 저장, 존재 여부 확인
#
# 3. defaultdict
#    기본값이 자동으로 들어가는 dict
#
# 4. Counter
#    빈도수 세기에 특화된 자료구조


from collections import defaultdict, Counter


# --------------------------------------------------
# [4. dict 기본]
# --------------------------------------------------
# dict는 key -> value 구조다.

mp = {}
mp["apple"] = 3
mp["banana"] = 5

apple_count = mp["apple"]       # 3
banana_count = mp.get("banana") # 5
orange_count = mp.get("orange", 0)  # key가 없으면 0 반환

# 존재 확인
has_apple = "apple" in mp
has_orange = "orange" in mp

# 순회
for key in mp:
    value = mp[key]
    pass

for key, value in mp.items():
    pass

# 삭제
del mp["banana"]


# --------------------------------------------------
# [5. set 기본]
# --------------------------------------------------
# set은 중복 없는 값 집합이다.
# 빠른 존재 여부 확인에 강하다.

s = set()
s.add(3)
s.add(5)
s.add(3)  # 중복 추가해도 하나만 유지

has_3 = 3 in s
has_10 = 10 in s

s.discard(10)  # 없어도 에러 없음

# 중복 제거
nums = [1, 2, 2, 3, 3, 3]
unique_nums = list(set(nums))

# 주의: set은 순서를 보장하지 않는다.


# --------------------------------------------------
# [6. 가장 자주 쓰는 패턴 1 - 빈도수 세기]
# --------------------------------------------------
# Hash의 대표 사용법이다.

arr = [1, 3, 2, 1, 3, 1, 5]

freq = {}
for x in arr:
    if x not in freq:
        freq[x] = 0
    freq[x] += 1

# 결과 예시:
# {1: 3, 3: 2, 2: 1, 5: 1}

# defaultdict를 쓰면 더 간단하다.
freq_dd = defaultdict(int)
for x in arr:
    freq_dd[x] += 1

# Counter를 쓰면 더 짧다.
freq_counter = Counter(arr)

# 확인
count_of_1 = freq_dd[1]
count_of_3 = freq_counter[3]


# --------------------------------------------------
# [7. 가장 자주 쓰는 패턴 2 - 존재 여부 확인]
# --------------------------------------------------
# "이미 나온 적 있나?" 같은 문제에서 자주 쓴다.

nums = [3, 5, 7, 3, 1]

seen = set()
has_duplicate = False

for x in nums:
    if x in seen:
        has_duplicate = True
        break
    seen.add(x)


# --------------------------------------------------
# [8. 가장 자주 쓰는 패턴 3 - 값 -> 인덱스 저장]
# --------------------------------------------------
# two sum 같은 문제에서 자주 나온다.

nums = [2, 7, 11, 15]
target = 9

index_map = {}
answer = None

for i, num in enumerate(nums):
    need = target - num
    if need in index_map:
        answer = [index_map[need], i]
        break
    index_map[num] = i

# answer == [0, 1]


