# --------------------------------------------------
# [11. defaultdict]
# --------------------------------------------------
# 존재하지 않는 key 접근 시 기본값을 자동으로 넣는다.

freq = defaultdict(int)
freq[10] += 1
freq[10] += 1

# int -> 기본값 0
# list -> 기본값 []
# set -> 기본값 set()

graph = defaultdict(list)
