# [14. 대표 문제 유형]
# --------------------------------------------------
# Hash는 아래 유형으로 자주 나온다.
#
# 1) 중복 확인
#    - 배열에 중복 원소가 있는가?
#    - 방문한 적 있는 상태인가?
#
# 2) 빈도수 세기
#    - 가장 많이 나온 값은?
#    - 각 단어가 몇 번 나왔는가?
#
# 3) 매핑
#    - 이름 -> 점수
#    - 값 -> 인덱스
#    - 유저 -> 상태
#
# 4) 문자열 처리
#    - 아나그램
#    - 문자 빈도 비교
#
# 5) 전처리 후 빠른 탐색
#    - 미리 set/dict에 저장한 뒤 빠르게 검사


# --------------------------------------------------
# [15. 자주 하는 실수]
# --------------------------------------------------
# 1) dict는 정렬되지 않는다고 생각해야 한다.
#    Python 3.7+에서는 삽입 순서를 유지하지만,
#    "정렬"된 것은 아니다.
#    정렬이 필요하면 sorted()를 써야 한다.
#
# 2) set도 순서를 기대하면 안 된다.
#
# 3) 없는 key를 바로 조회하면 KeyError가 날 수 있다.
#    예:
#    mp["orange"]   # key가 없으면 에러 가능
#
#    안전하게 쓰려면:
#    mp.get("orange", 0)
#    또는 defaultdict 사용
#
# 4) 빈도수 세기를 list로 억지로 하려다 느려질 수 있다.
#    key 범위가 크거나 문자열이면 dict가 훨씬 자연스럽다.
#
# 5) Counter가 편하다고 무조건 쓰기보다,
#    직접 dict/defaultdict가 더 직관적인 상황도 있다.


# --------------------------------------------------
# [16. list로 풀 수 있는데 왜 Hash를 쓰는가]
# --------------------------------------------------
# 예를 들어 값의 범위가 0 ~ 100 정도로 매우 작다면
# 배열(list)로 카운팅할 수도 있다.
#
# count = [0] * 101
# for x in arr:
#     count[x] += 1
#
# 하지만 아래 상황에서는 Hash가 훨씬 자연스럽다.
#
# - 값의 범위가 매우 크다
# - 음수가 포함된다
# - 문자열을 key로 써야 한다
# - 등장한 값만 관리하고 싶다


