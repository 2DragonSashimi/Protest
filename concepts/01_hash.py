# --------------------------------------------------
# [18. 실전 템플릿 1 - 빈도수 세기]
# --------------------------------------------------
def count_frequency(arr):
    freq = defaultdict(int)
    for x in arr:
        freq[x] += 1
    return freq


# 사용 예시
example_freq = count_frequency([1, 2, 2, 3, 3, 3])


# --------------------------------------------------
# [19. 실전 템플릿 2 - 중복 검사]
# --------------------------------------------------
def has_duplicate(arr):
    seen = set()
    for x in arr:
        if x in seen:
            return True
        seen.add(x)
    return False


# 사용 예시
dup_result = has_duplicate([1, 2, 3, 4, 2])


# --------------------------------------------------
# [20. 실전 템플릿 3 - two sum]
# --------------------------------------------------
def two_sum(nums, target):
    pos = {}
    for i, num in enumerate(nums):
        need = target - num
        if need in pos:
            return [pos[need], i]
        pos[num] = i
    return []


# 사용 예시
two_sum_result = two_sum([2, 7, 11, 15], 9)


# --------------------------------------------------
# [21. 실전 템플릿 4 - 아나그램 판별]
# --------------------------------------------------
def is_anagram_func(a, b):
    return Counter(a) == Counter(b)


# 사용 예시
anagram_result = is_anagram_func("listen", "silent")


