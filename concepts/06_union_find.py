# ============================================
# 06. Union-Find
# ============================================

# --------------------------------------------------
# [1. 왜 필요한가]
# --------------------------------------------------
# Union-Find(Disjoint Set Union, DSU)는
# "원소들이 어떤 집합에 속해 있는지"를 매우 빠르게 관리하는 자료구조다.
#
# 핵심 기능은 두 가지다.
# 1) 두 원소가 같은 집합에 속하는지 확인
# 2) 두 집합을 하나로 합치기
#
# 예:
# - 연결 여부 판별
# - 연결 요소 관리
# - 사이클 판별
# - 네트워크 묶기
# - Kruskal MST
#
# 그래프를 DFS/BFS로 탐색하지 않고도
# "집합 단위"로 연결 관계를 관리할 수 있다는 점이 핵심이다.


# --------------------------------------------------
# [2. 언제 떠올려야 하는가]
# --------------------------------------------------
# 문제를 읽다가 아래 신호가 보이면 Union-Find를 먼저 의심한다.
#
# - 두 원소가 같은 그룹인가?
# - 집합을 계속 합쳐야 하는가?
# - 연결 여부 질의가 많이 들어오는가?
# - 간선을 추가하면서 사이클이 생기는지 확인해야 하는가?
# - 여러 노드를 그룹/컴포넌트 단위로 관리해야 하는가?
#
# 대표 예시:
# - 서로소 집합 기본 문제
# - 네트워크 연결
# - 친구 관계 묶기
# - 사이클 판별
# - 크루스칼 최소 스패닝 트리


# --------------------------------------------------
# [3. 핵심 아이디어]
# --------------------------------------------------
# 각 원소는 parent를 가진다.
# parent를 따라 올라가면 그 집합의 대표(root)를 찾을 수 있다.
#
# 핵심 연산:
# 1) find(x)
#    - x가 속한 집합의 대표(root)를 찾는다
#
# 2) union(a, b)
#    - a와 b가 속한 두 집합을 하나로 합친다
#
# 즉,
# "대표가 같으면 같은 집합"
# 이라고 이해하면 된다.


# --------------------------------------------------
# [4. 기본 구조]
# --------------------------------------------------
# 처음에는 각 원소가 자기 자신만의 집합이다.

n = 7
parent = [i for i in range(n + 1)]

# parent[x] == x 이면 x가 집합의 대표(root)라는 뜻


# --------------------------------------------------
# [5. find - 기본]
# --------------------------------------------------
# x의 대표(root)를 찾는다.

def find_basic(x):
    while parent[x] != x:
        x = parent[x]
    return x


# --------------------------------------------------
# [6. union - 기본]
# --------------------------------------------------
# 두 원소가 속한 집합을 합친다.

def union_basic(a, b):
    ra = find_basic(a)
    rb = find_basic(b)

    if ra != rb:
        parent[rb] = ra


# 사용 예시
union_basic(1, 2)
union_basic(2, 3)

same_group_1_3 = (find_basic(1) == find_basic(3))   # True
same_group_1_4 = (find_basic(1) == find_basic(4))   # False


# --------------------------------------------------
# [7. 문제점 - find가 느려질 수 있음]
# --------------------------------------------------
# 단순 구현만 하면 트리가 한쪽으로 치우쳐서
# find가 느려질 수 있다.
#
# 예:
# 1 <- 2 <- 3 <- 4 <- 5
#
# 이런 경우 find(5)는 parent를 계속 타고 올라가야 한다.
#
# 이를 개선하기 위해 두 가지를 쓴다.
# 1) path compression
# 2) union by rank / size


# --------------------------------------------------
# [8. 경로 압축 (Path Compression)]
# --------------------------------------------------
# find를 할 때 중간 노드들의 parent를 대표(root)로 바로 연결한다.
# 이렇게 하면 이후 find가 매우 빨라진다.

parent = [i for i in range(n + 1)]

def find(x):
    if parent[x] != x:
        parent[x] = find(parent[x])
    return parent[x]


# --------------------------------------------------
# [9. union - 경로 압축 버전]
# --------------------------------------------------
def union(a, b):
    ra = find(a)
    rb = find(b)

    if ra != rb:
        parent[rb] = ra


# --------------------------------------------------
# [10. union by size]
# --------------------------------------------------
# 더 큰 집합 아래로 작은 집합을 붙이면 트리 높이를 줄이기 쉽다.

parent = [i for i in range(n + 1)]
size = [1] * (n + 1)

def find_size(x):
    if parent[x] != x:
        parent[x] = find_size(parent[x])
    return parent[x]

def union_size(a, b):
    ra = find_size(a)
    rb = find_size(b)

    if ra == rb:
        return False

    if size[ra] < size[rb]:
        ra, rb = rb, ra

    parent[rb] = ra
    size[ra] += size[rb]
    return True

# return True  -> 실제로 합쳐짐
# return False -> 이미 같은 집합


# --------------------------------------------------
# [11. 같은 집합인지 확인]
# --------------------------------------------------
def is_same_set(a, b):
    return find_size(a) == find_size(b)


# --------------------------------------------------
# [12. Union-Find의 핵심 감각]
# --------------------------------------------------
# DFS/BFS는 "직접 탐색"해서 연결 관계를 본다.
# Union-Find는 "집합 대표"를 관리해서 연결 관계를 본다.
#
# 즉,
# DFS/BFS = 탐색 중심
# Union-Find = 집합 관리 중심
#
# 문제에서 연결 여부 질의가 많으면
# Union-Find가 특히 강하다.


# --------------------------------------------------
# [13. 가장 자주 쓰는 패턴 1 - 연결 여부 질의]
# --------------------------------------------------
# 간선을 계속 추가하고,
# 두 노드가 같은 그룹인지 자주 물어보는 문제.

n = 5
parent = [i for i in range(n + 1)]
size = [1] * (n + 1)

union_size(1, 2)
union_size(2, 3)

query_1_3 = is_same_set(1, 3)   # True
query_1_5 = is_same_set(1, 5)   # False


# --------------------------------------------------
# [14. 가장 자주 쓰는 패턴 2 - 연결 요소 개수]
# --------------------------------------------------
# 모든 union이 끝난 뒤 대표(root)의 개수를 세면 연결 요소 개수가 된다.

def count_components(n):
    roots = set()
    for x in range(1, n + 1):
        roots.add(find_size(x))
    return len(roots)


# --------------------------------------------------
# [15. 가장 자주 쓰는 패턴 3 - 사이클 판별]
# --------------------------------------------------
# 무방향 그래프에서 간선을 추가할 때
# 두 끝점이 이미 같은 집합이면 사이클이 생긴다.

def has_cycle(n, edges):
    parent = [i for i in range(n + 1)]
    size = [1] * (n + 1)

    def find_local(x):
        if parent[x] != x:
            parent[x] = find_local(parent[x])
        return parent[x]

    def union_local(a, b):
        ra = find_local(a)
        rb = find_local(b)

        if ra == rb:
            return False

        if size[ra] < size[rb]:
            ra, rb = rb, ra

        parent[rb] = ra
        size[ra] += size[rb]
        return True

    for a, b in edges:
        if not union_local(a, b):
            return True

    return False


cycle_result_1 = has_cycle(3, [(1, 2), (2, 3), (1, 3)])  # True
cycle_result_2 = has_cycle(3, [(1, 2), (2, 3)])          # False


# --------------------------------------------------
# [16. Kruskal과의 연결]
# --------------------------------------------------
# Kruskal 최소 스패닝 트리에서는
# 간선을 가중치 순으로 보면서
# 두 끝점이 다른 집합일 때만 선택한다.
#
# 즉, Union-Find는 Kruskal의 핵심 도구다.
#
# 흐름:
# 1) 간선을 가중치 순 정렬
# 2) 작은 간선부터 확인
# 3) 두 끝점이 다른 집합이면 union + 선택
# 4) 같은 집합이면 사이클이 생기므로 건너뜀


# --------------------------------------------------
# [17. 시간복잡도]
# --------------------------------------------------
# path compression + union by size/rank를 같이 쓰면
# find / union은 거의 O(1)에 가깝게 매우 빠르다.
#
# 이론적으로는 아커만 역함수(alpha(N)) 정도인데,
# 코테에서는 사실상 상수처럼 생각해도 된다.


# --------------------------------------------------
# [18. 대표 문제 유형]
# --------------------------------------------------
# Union-Find는 아래 유형으로 자주 나온다.
#
# 1) 서로 같은 집합인지 확인
#    - 같은 팀인가?
#    - 같은 네트워크인가?
#
# 2) 집합 병합
#    - 친구 관계 합치기
#    - 그룹 묶기
#
# 3) 연결 요소 개수
#    - 몇 개의 컴포넌트가 있는가?
#
# 4) 사이클 판별
#    - 간선 추가 시 사이클 여부
#
# 5) MST
#    - Kruskal
#
# 6) 동적 연결 관리
#    - union과 query가 섞여 들어오는 문제


# --------------------------------------------------
# [19. DFS/BFS와 Union-Find 비교]
# --------------------------------------------------
# DFS/BFS가 더 자연스러운 경우:
# - 실제 경로/순회가 중요
# - 탐색 순서가 중요
# - 최단거리 구하기
# - 격자 탐색
#
# Union-Find가 더 자연스러운 경우:
# - 연결 여부 질의가 많음
# - 집합 병합이 많음
# - 사이클 판별
# - 간선을 순서대로 추가하며 관리
#
# 즉, "연결만 중요"하면 Union-Find를 먼저 떠올릴 수 있다.


# --------------------------------------------------
# [20. 자주 하는 실수]
# --------------------------------------------------
# 1) parent 초기화를 빼먹음
#    -> parent[i] = i 가 기본
#
# 2) find 결과가 아니라 원래 원소끼리 비교함
#    -> 항상 대표(root)끼리 비교해야 함
#
# 3) union할 때 이미 같은 집합인지 체크 안 함
#
# 4) 경로 압축 없이 구현해서 비효율적이 됨
#
# 5) size/rank 갱신 실수
#
# 6) 0-index / 1-index 혼동
#
# 7) 방향 그래프 문제에 무턱대고 적용
#    -> Union-Find는 보통 "무방향 연결" 감각에 더 자연스럽다
#
# 8) 연결 요소 개수 셀 때 find를 안 돌리고 parent만 바로 set에 넣음
#    -> 반드시 find(x)로 대표를 모아야 함


# --------------------------------------------------
# [21. 문제를 보고 Union-Find를 떠올리는 체크리스트]
# --------------------------------------------------
# 아래 질문 중 하나라도 "예"면 Union-Find 후보일 가능성이 높다.
#
# - 두 원소가 같은 그룹인지 자주 물어보는가?
# - 집합을 계속 합쳐야 하는가?
# - 간선을 추가하면서 연결 관계를 관리하는가?
# - 사이클 생김 여부를 판별해야 하는가?
# - 연결만 중요하고 실제 경로는 중요하지 않은가?
# - Kruskal/MST 느낌인가?


# --------------------------------------------------
# [22. 실전 템플릿 1 - 기본 Union-Find]
# --------------------------------------------------
class UnionFind:
    def __init__(self, n):
        self.parent = list(range(n + 1))
        self.size = [1] * (n + 1)

    def find(self, x):
        if self.parent[x] != x:
            self.parent[x] = self.find(self.parent[x])
        return self.parent[x]

    def union(self, a, b):
        ra = self.find(a)
        rb = self.find(b)

        if ra == rb:
            return False

        if self.size[ra] < self.size[rb]:
            ra, rb = rb, ra

        self.parent[rb] = ra
        self.size[ra] += self.size[rb]
        return True

    def same(self, a, b):
        return self.find(a) == self.find(b)

    def component_size(self, x):
        return self.size[self.find(x)]


uf = UnionFind(7)
uf.union(1, 2)
uf.union(2, 3)
uf_same_result = uf.same(1, 3)          # True
uf_size_result = uf.component_size(1)   # 3


# --------------------------------------------------
# [23. 실전 템플릿 2 - 연결 요소 개수]
# --------------------------------------------------
def count_components_uf(n, edges):
    uf = UnionFind(n)

    for a, b in edges:
        uf.union(a, b)

    roots = set()
    for x in range(1, n + 1):
        roots.add(uf.find(x))

    return len(roots)


components_result = count_components_uf(5, [(1, 2), (2, 3), (4, 5)])  # 2


# --------------------------------------------------
# [24. 실전 템플릿 3 - 사이클 판별]
# --------------------------------------------------
def detect_cycle_uf(n, edges):
    uf = UnionFind(n)

    for a, b in edges:
        if uf.same(a, b):
            return True
        uf.union(a, b)

    return False


cycle_detect_result = detect_cycle_uf(3, [(1, 2), (2, 3), (1, 3)])  # True


# --------------------------------------------------
# [25. 실전 템플릿 4 - 질의 처리]
# --------------------------------------------------
# query 예시:
# 0 a b -> union(a, b)
# 1 a b -> same(a, b) 결과 출력

def process_queries(n, queries):
    uf = UnionFind(n)
    result = []

    for t, a, b in queries:
        if t == 0:
            uf.union(a, b)
        else:
            result.append(uf.same(a, b))

    return result


query_result = process_queries(
    7,
    [
        (0, 1, 3),
        (1, 1, 7),
        (0, 7, 6),
        (1, 7, 1),
        (0, 3, 7),
        (1, 1, 7),
    ]
)


# --------------------------------------------------
# [26. 실전 템플릿 5 - Kruskal MST]
# --------------------------------------------------
def kruskal_mst(n, edges):
    # edges: (cost, a, b)
    edges.sort()
    uf = UnionFind(n)

    mst_cost = 0
    used = 0

    for cost, a, b in edges:
        if uf.union(a, b):
            mst_cost += cost
            used += 1
            if used == n - 1:
                break

    return mst_cost


mst_result = kruskal_mst(
    4,
    [
        (1, 1, 2),
        (4, 1, 3),
        (2, 2, 3),
        (3, 2, 4),
        (5, 3, 4),
    ]
)


