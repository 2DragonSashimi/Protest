# [24. 자주 하는 실수]
# --------------------------------------------------
# 1) 문자열은 immutable이다.
#    즉, 문자 하나를 직접 수정할 수 없다.
#
# 2) 반복문에서 문자열 += 를 너무 많이 하면 비효율적일 수 있다.
#    -> list에 append 후 join 고려
#
# 3) split()과 split(" ")는 다르다.
#    split()은 연속 공백도 잘 처리하지만,
#    split(" ")는 공백 하나 기준이라 결과가 다를 수 있다.
#
# 4) find()는 없으면 -1을 반환한다.
#    이를 인덱스로 바로 사용하면 실수할 수 있다.
#
# 5) 숫자 변환 전에 형식이 맞는지 확인해야 할 때가 있다.
#    예: int("12a") 는 에러
#
# 6) 정렬 비교와 Counter 비교의 차이를 이해해야 한다.
#    - sorted(s1) == sorted(s2)
#    - Counter(s1) == Counter(s2)
#    둘 다 가능하지만 의도가 다를 수 있다.


