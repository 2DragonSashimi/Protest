# [29. 실전 템플릿 4 - 문자열 압축 느낌]
# --------------------------------------------------
def compress_string(s):
    if not s:
        return ""

    result = []
    prev = s[0]
    count = 1

    for i in range(1, len(s)):
        if s[i] == prev:
            count += 1
        else:
            result.append(prev)
            result.append(str(count))
            prev = s[i]
            count = 1

    result.append(prev)
    result.append(str(count))
    return "".join(result)


compressed = compress_string("aaabbcccc")


